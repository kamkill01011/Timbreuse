package ch.cpnv.timbreuse.beans;

public class Student {
	private String lastname;
	private int diff;
	
	public String getLastname() {
		return lastname;
	}
	public void setLastname(String name) {
		this.lastname = name;
	}
	public int getDiff() {
		return diff;
	}
	public void setDiff(int diff) {
		this.diff = diff;
	}
}
